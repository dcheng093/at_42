#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 2;
	while (nb >= i * i)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}

int	main(void)
{
	printf("%d\n", ft_sqrt(24));
}
