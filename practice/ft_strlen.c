#include <stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		i++;
	}
	return (i);
}

int	main(void)
{
	char	*str = "test";
	int	length = ft_strlen(str);
	printf("%d\n", length);
}